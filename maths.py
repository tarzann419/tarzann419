pi = 3.142
class Math:
    def areaoftriangle(self,base, height):
        return (0.5*base*height)

    def areaofcylinder(self, length, width):
        return (length*width)

    def volumeofcone(self, radius, height):
        return (0.33*pi*radius**2*height)

    def areaofrectangle(self, length, breath):
        return (length*breath)
